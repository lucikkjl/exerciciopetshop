public class Animal {
    
    int IdAnimal;
    String nomeAnimal;
    String raca;
    int peso;
    int IdCategoria;
    int IdTutor;


    public Animal(

        int IdAnimal,
        String nomeAnimal,
        String raca,
        int peso,
        int IdCategoria,
        int IdTutor
    ){

        {
            this.IdAnimal = IdAnimal;
            this.nomeAnimal = nomeAnimal;
            this.raca = raca;
            this.peso = peso;
            this.IdCategoria = IdCategoria;
            this.IdTutor = IdTutor;
        }

    }




}
